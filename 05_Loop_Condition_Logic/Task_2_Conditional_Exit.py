# Initialize a counter
counter = 0

# Loop until counter reaches 5
while counter < 5:  # Loop continues as long as counter is less than 5
    print("Looping until the condition is met.")
    counter += 1  # Update the counter in each iteration

# the loop condition (while counter < 5) is directly tied to the 'counter' variable.
# Initially set to 0, the counter is incremented each time the loop runs. When the counter reaches 5,
# the condition (counter < 5) becomes False, and the loop exits naturally.
# This demonstrates how the loop condition, based on variable states that change during loop execution,
# determines when the loop exits.